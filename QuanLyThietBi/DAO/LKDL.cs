    class LKDL
    {
        private static LKDL instance;

        public static LKDL Instance
        {
            get { if (instance == null) instance = new LKDL(); return LKDL.instance; }
            private set { LKDL.instance = value; }
        }

        private LKDL() { }

        private string connectionSTR = "Data Source=CONG-MINH\\SQLEXPRESS;Initial Catalog=QuanLyThietBi;Integrated Security=True";

        //Trả dữ liệu ra kiểu list
        public DataTable ExecuteQuery(string query, object[] parameter = null)
        {
            DataTable data = new DataTable();
            using (SqlConnection connection = new SqlConnection(connectionSTR))
            {
                connection.Open();
                SqlCommand command = new SqlCommand(query, connection);
                if (parameter != null)
                {
                    string[] listPara = query.Split(' ');
                    int i = 0;
                    foreach (string item in listPara)
                    {
                        if (item.Contains('@'))
                        {
                            command.Parameters.AddWithValue(item, parameter[i]);
                            i++;
                        }
                    }
                }
                SqlDataAdapter adapter = new SqlDataAdapter(command);
                adapter.Fill(data);
                connection.Close();
            }
            return data;
        }

